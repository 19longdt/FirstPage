package Entity;

/**
 *
 * @author hp
 */
public class Product {

    private int pid, amount, cid;
    private String pname, img, description;
    private double price;

    public Product() {
    }

    public Product(int pid, String pname, double price, int amount, String img, int cid, String description) {
        this.pid = pid;
        this.amount = amount;
        this.cid = cid;
        this.pname = pname;
        this.img = img;
        this.price = price;
        this.description = description;
    }

    public int getPid() {
        return pid;
    }

    public void setPid(int pid) {
        this.pid = pid;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
